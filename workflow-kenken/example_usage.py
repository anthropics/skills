#!/usr/bin/env python3
"""
ワークフロー使用例
"""

# === 単一企業の例 ===

# 契約フェーズの実行
print("起きろ")
# → 以下の情報を入力:
# クライアント名: 合同会社大吉
# 業務内容: 生成AIアドバイザリー
# 月額金額: 300,000円
# 契約期間: 2025年12月〜2026年2月（3ヶ月）
# 業種: サービス業
# 従業員数: 5名以下
# 対象地域: 香川県
# 導入目的: 生成AI導入

# 月次処理の実行
print("月締め")
# → 以下の情報を入力:
# クライアント名: 合同会社大吉
# 対象月: 2025年12月
# 請求金額: 300,000円（税別）
# 支払期限: 2026年1月31日

# 全フェーズの実行
print("フル実行")
# → 契約フェーズ＋実行フェーズの情報をまとめて入力

# === パラレル実行の例 ===

# 3社分の契約フェーズを並列実行
print("起きろ×3")
# → 3社分の情報を入力:
# 【企業1】
# クライアント名: 合同会社大吉
# 業務内容: 生成AIアドバイザリー
# 月額金額: 300,000円
# 契約期間: 3ヶ月
# 業種: サービス業
#
# 【企業2】
# クライアント名: 株式会社○○製作所
# 業務内容: IT導入支援
# 月額金額: 200,000円
# 契約期間: 6ヶ月
# 業種: 製造業
#
# 【企業3】
# クライアント名: △△商店
# 業務内容: DX推進支援
# 月額金額: 150,000円
# 契約期間: 12ヶ月
# 業種: 小売業

# 5社分の月次処理を並列実行
print("月締め×5")
# → 5社分の対象月と請求金額を入力

