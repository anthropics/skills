def theme_mode(theme):
    """Optional 6th tuple element: 'dark' or 'light' (default 'light')."""
    return theme[5] if len(theme) > 5 else "light"


def render(theme):
    name, desc, category, colors, best_for = theme[:5]
    mode = theme_mode(theme)
    header_font, body_font = CATEGORY_FONTS[category]

    # Light themes run lightest -> darkest (background -> text); dark themes run
    # darkest -> lightest (background -> foreground).
    ordered = sorted(colors, key=lambda c: luminance(c[1]),
                     reverse=(mode != "dark"))
    ladder_set = LADDERS[mode]
    ladder = ladder_set.get(len(ordered), ladder_set[6])

    lines = [f"# {name}", "", desc, "", "## Color Palette", ""]
    for (cname, chex), role in zip(ordered, ladder):
        lines.append(f"- **{cname}**: `{chex.upper()}` - {role}")
    lines += [
        "",
        "## Typography",
        "",
        f"- **Headers**: {header_font} (use {header_font} Bold for emphasis)",
        f"- **Body Text**: {body_font}",
        "",
        "## Best Used For",
        "",
        best_for,
        "",
        f"> Category: {category}  ·  Mode: {mode}",
        "",
    ]
    return "\n".join(lines)


def main():
    os.makedirs(THEMES_DIR, exist_ok=True)
    for theme in THEMES:
        slug = slugify(theme[0])
        path = os.path.join(THEMES_DIR, f"{slug}.md")
        with open(path, "w") as f:
            f.write(render(theme))
    print(f"Wrote {len(THEMES)} theme files to {THEMES_DIR}")


if __name__ == "__main__":
    main()
