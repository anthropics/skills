# Role ladders by palette size. For light themes colors run lightest -> darkest
# (background -> text); for dark themes they run darkest -> lightest
# (background -> foreground).
ROLE_LADDERS = {
    6: ["Base / background", "Light neutral", "Soft accent",
        "Mid-tone accent", "Deep accent", "Anchor / text"],
    5: ["Base / background", "Light neutral", "Accent",
        "Deep accent", "Anchor / text"],
    4: ["Base / background", "Light accent", "Deep accent", "Anchor / text"],
}
DARK_LADDERS = {
    6: ["Base / background", "Surface", "Muted accent",
        "Accent", "Bright accent", "Foreground / text"],
    5: ["Base / background", "Surface", "Accent",
        "Bright accent", "Foreground / text"],
    4: ["Base / background", "Accent", "Bright accent", "Foreground / text"],
}
LADDERS = {"light": ROLE_LADDERS, "dark": DARK_LADDERS}


def luminance(hex_code):
    """Relative luminance (0=dark, 1=light) for sorting."""
    h = hex_code.lstrip("#")
    r, g, b = (int(h[i:i + 2], 16) / 255 for i in (0, 2, 4))
    return 0.2126 * r + 0.7152 * g + 0.0722 * b


def slugify(name):
    s = name.lower()
    s = re.sub(r"[^a-z0-9]+", "-", s).strip("-")
    return s


